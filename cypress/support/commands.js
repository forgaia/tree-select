Cypress.Commands.add('login', () => {
  cy.visit('https://localhost:4000/login')
    .reload() // The reload is a hack solution for this known bug: https://github.com/cypress-io/cypress/issues/1297 happening because things are running so fast.
    .location('pathname', { timeout: 10000 })
    .should('eq', '/patientmodel/');
});
